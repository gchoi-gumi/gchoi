#include "main_header.h"

int	add(int i, int j)
{
	return (i + j);
}

int	sub(int i, int j)
{
	return (i - j);
}

int	mul(int i, int j)
{
	return (i * j);
}

int	div(int i, int j)
{
	return (i / j);
}

int	mod(int i, int j)
{
	return (i % j);
}
